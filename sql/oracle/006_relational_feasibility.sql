CREATE TABLE feasibility_run_partners (
  id                  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  run_id              NUMBER NOT NULL REFERENCES feasibility_runs(id) ON DELETE CASCADE,
  partner_order       NUMBER NOT NULL,
  partner_name        VARCHAR2(255 CHAR) NOT NULL,
  share_pct           NUMBER,
  profit_share        NUMBER NOT NULL,
  CONSTRAINT uq_feasibility_run_partners UNIQUE (run_id, partner_order)
);

CREATE INDEX idx_feasibility_run_partners_run
  ON feasibility_run_partners (run_id, partner_order);

